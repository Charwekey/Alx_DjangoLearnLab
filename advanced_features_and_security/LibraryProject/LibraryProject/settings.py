def _env_bool(name, default=False):
    """Return boolean from environment variables. Treats '1', 'true', 'yes'
    (case-insensitive) as True."""
    v = os.environ.get(name)
    if v is None:
        return default
    return str(v).lower() in ("1", "true", "yes")


